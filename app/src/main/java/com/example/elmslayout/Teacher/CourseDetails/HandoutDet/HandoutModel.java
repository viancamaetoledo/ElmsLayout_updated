package com.example.elmslayout.Teacher.CourseDetails.HandoutDet;

public class HandoutModel {
    private String handoutNo, title, period, filePath, id;

    public HandoutModel() {
    }

    public String getHandoutNo() { return handoutNo; }
    public void setHandoutNo(String handoutNo) { this.handoutNo = handoutNo; }

    public String getTitle() { return title; }
    public void setTitle(String title) { this.title = title; }

    public String getPeriod() { return period; }
    public void setPeriod(String period) { this.period = period; }

    public String getFilePath() { return filePath; }
    public void setFilePath(String filePath) { this.filePath = filePath; }

    public String getId() { return id; }
    public void setId(String id) { this.id = id; }
}
